    <h2>L’aide aux aidants</h2>
    <div class="underline"></div>
    <p class="important">Je ne prend plus de nouveaux patients en suivi actuellement. ll reste possible de réaliser des
        bilans neuropsychologiques et/ou psychométriques. </p>

    <p><strong>Accompagner un proche ou un parent dépendant</strong>n’est
        <strong>pas une tâche aisée</strong> et cet accompagnement créé de <strong>nombreux bouleversements</strong>
        dans la vie des aidants. Il est donc parfois nécessaire pour les aidants eux-mêmes se faire aider.
    </p>
    <p>
        Aujourd’hui, près de 11 millions de personnes en France sont des aidants. Ils assistent leur proche dans tous
        les actes de la vie quotidienne et doivent <strong>faire face à la maladie, au handicap et/ou à la vieillesse du
            proche</strong>.
        Les capacités et les ressources des aidants ne sont pas infinies et il est primordial de considérer le risque
        psychologique de leur investissement.
    </p>

    <div class="imgglobal">
        <img src="./public/medias/Illustration-aide-aux-aidants-300x251.png" alt="Illustration page psychoéducation">
    </div>
    <p><span class="important">En pratique</span>, l l’aide aux aidants vise à <strong>soutenir, accompagner et informer
            les
            aidants</strong> dans et sur leur prise en charge de leur parent ou leur proche.</p>
    <p>
        L’objectif principal est de <strong>prévenir l’épuisement psychologique</strong> des aidants en leur proposant
        un espace d’écoute
        pour <strong>mieux comprendre et accepter leur situation</strong> ainsi que celle de leur proche et ainsi
        pouvoir <strong>trouver les
            ressources utiles </strong>à la poursuite de leur prise en charge.
    </p>

</div>