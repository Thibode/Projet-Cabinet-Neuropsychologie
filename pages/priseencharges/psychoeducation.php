    <h2>La psychoéducation</h2>
    <div class="underline"></div>
    <p class="important">Je ne prend plus de nouveaux patients en suivi actuellement. ll reste possible de réaliser des
        bilans neuropsychologiques et/ou psychométriques. </p>

    <p>La psychoéducation, ou <strong>éducation thérapeutique</strong>, a pour but de permettre au patient de
        <strong>mieux
            comprendre la maladie
            et les symptômes</strong> qui lui sont associés.
    </p>
    <p>
        Il s’agit pour le patient de pouvoir <strong>être actif dans le processus de soins</strong> et de prise en
        charge de
        la maladie
        de façon à <strong>adopter les bons comportements</strong> au quotidien. Grâce à la psychoéducation, il est
        amené à
        <strong>devenir
            expert</strong> de sa pathologie.
    </p>

    <div class="imgglobal">
        <img src="./public/medias/Illustration-psychoéducation.png" alt="Illustration page psychoéducation">
    </div>
    <p><span class="important">En pratique</span>, le patient <strong>acquiert toutes les connaissances
            nécessaires</strong> sur sa
        pathologie, ses troubles associés et leurs évolutions. Cette prise en charge vise également à apprendre à
        surveiller les symptômes précoces qui peuvent prédire une phase aigüe, les symptômes résiduels ou les symptômes
        des nouveaux épisodes de la maladie.</p>
    <p>
        La psychoéducation est <strong>également destinée aux proches</strong> du patient. Les séances peuvent se
        dérouler en
        co-participation avec le patient ou bien en séance individuelle.
    </p>

</div>