    <h2>La remédiation cognitive</h2>
    <div class="underline"></div>
    <p class="important">Je ne prend plus de nouveaux patients en suivi actuellement. ll reste possible de réaliser des
        bilans neuropsychologiques et/ou psychométriques. </p>

    <p>La remédiation cognitive est un <strong>outil thérapeutique</strong> utilisé dans le cadre de pathologies dans
        lesquelles s’expriment des troubles cognitifs.</p>
    <p>
        Les déficits cognitifs sont fréquemment associés à des pathologies neurologiques, développementales ou
        psychiatriques. Ils se manifestent par des <strong>troubles de l’attention, de la mémoire, des fonctions
            exécutives</strong>
        (raisonnement, organisation, planification, flexibilité mentale, …),<strong> des praxies</strong> (organisation
        des gestes),<strong> des
            gnosies </strong>(perception visuelle ou auditive),<strong> de la cognition sociale </strong>(relative aux
        relations sociales) ou encore
        <strong>du langage</strong>.
    </p>
    <p>
        La remédiation cognitive vise donc à <strong>soutenir les capacités cognitives affaiblies</strong> mais surtout
        à <strong>apprendre et à
            utiliser des stratégies compensatoires</strong> qui vont <strong>s’appuyer sur les points forts du patient
            pour pallier à ses
            troubles cognitifs</strong>. L’objectif est aussi de <strong>mettre en place ces stratégies au
            quotidien</strong> et d’aménager
        l’environnement grâce à des <strong>outils suppléants</strong> les fonctions déficitaires de façon à améliorer
        la qualité de vie
        du patient.
    </p>
    <div class="imgglobal">
        <img src="./public/medias/Illustration-remédiation.png" alt="Illustration page remédiation">
    </div>
    <p><span class="important">En pratique</span>, la remédiation cognitive se fait <strong>uniquement à la suite d’un
            bilan
            neuropsychologique complet</strong>. Elle
        est réalisée en lien avec les différents professionnels intervenants auprès du patient ainsi que de son
        entourage.</p>
    <p>
        Le neuropsychologue définit un <strong>programme de remédiation cognitive personnel et adapté</strong> au
        patient. Chaque
        programme comprend des <strong>exercices papiers-crayons et/ou informatiques</strong> permettant d’acquérir les
        stratégies
        compensatoires. Sont nécessairement incluent des <strong>exercices écologiques et des mises en
            situation</strong> dans
        l’environnement de la personne qui permettent le <strong>transfert des compétences</strong> dans le quotidien.
    </p>
    <p>
        La durée d’un programme de remédiation cognitive est <strong>variable selon la demande et les besoins</strong>
        du patient et peut
        durer de quelques semaines à plusieurs mois.
    </p>
    <p>
        A l’issue de la prise en charge en remédiation cognitive, un nouveau bilan peut être proposé afin d’observer
        l’évolution du patient et de ses troubles et d’établir si besoin l’indication d’une nouvelle prise en charge.
    </p>
