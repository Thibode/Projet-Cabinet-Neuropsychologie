    <h2>La Neuropsychologie</h2>
    <div class="underline"></div>
    <p>
        La neuropsychologie est une spécialité de la psychologie. C’est une
        discipline dont l’objectif est d’<strong>étudier les liens et les rapports entre les processus psychologiques
            supérieurs et le fonctionnement cérébral</strong>.
    </p>
    <div class="imgglobal">
        <img src="./public/medias/Illustration-neuropsychologie-1-300x270.png" alt="Illustration" />
    </div>
    <p>
        La neuropsychologie s’intéresse aux désordres des fonctions cognitives
        (intellectuelles), émotionnelles et comportementales survenant après une
        lésion cérébrale ainsi que dans le cadre d’une
        <strong>problématique neurodéveloppementale, neurodégénérative ou
            psychiatrique</strong>. La neuropsychologie étudie également les conséquences et les gênes
        fonctionnels pour le patient au quotidien.
    </p>
    <p>
        Dans ce cadre, le champ d’intervention du neuropsychologue est large et
        il peut prendre en charge tout type de patients : enfants, adolescents,
        adultes et séniors.
    </p>
    <p>
        Le neuropsychologue (ou psychologue spécialisé en neuropsychologie)
        détient un <strong>titre protégé</strong> par l’Etat suite à la
        validation d’une Licence et d’un Master II professionnel de Psychologie.
    </p>
    <div class="imgglobal">
        <img src="./public/medias/Illustration-neuropsychologie-2-300x236.png" alt="Illustration" />
    </div>
    <p>
        Il travaille en lien avec les autres professionnels : médicaux
        (neurologues, (pédo)psychiatres, gériatres, médecins traitants, …),
        paramédicaux (Infirmière Diplômée d’Etat, kinésithérapeutes,
        ergothérapeutes, psychomotriciens, orthophonistes, psychologues, …) et
        sociaux (assistants sociaux, éducateurs, …). Le neuropsychologue est
        également en lien avec les établissements de soins et de prise en
        charge.
    </p>
</div>