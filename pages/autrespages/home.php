    <div class="presentation">
        <h1>Présentation</h1>
        <p>
            Titulaire d’un Master II de Psychologie spécialisé en
            <strong>évaluations neuropsychologiques et cliniques</strong>, j’ai été
            formée à l’Université Catholique de l’Ouest d’Angers puis à l’Université
            de Nantes. J’ai également suivi les enseignements dispensés dans le
            cadre du Diplôme Universitaire de Remédiation Cognitive de l’Université
            Claude Bernard de Lyon 1.
        </p>

        <img src="./public/medias/Illustration-logo-300x257.png" />

        <p>
            Mes expériences professionnelles m’ont amené à me spécialiser dans
            <strong>la prise en charge des troubles cognitifs, comportementaux et
                psychologiques de l’enfant, de l’adolescent et de l’adulte.</strong>
        </p>
        <p>
            Je prend en charge les
            <strong>troubles neurodéveloppementaux</strong> chez l’enfant ou en
            diagnostic tardif chez l’adolescent ou l’adulte (troubles spécifiques
            des apprentissages, troubles « dys », troubles du spectre autistique,
            TDA-H, déficience intellectuelle, …),
            <strong>les pathologies neurologiques</strong> (traumatisme crânien,
            AVC, épilepsie, …) et
            <strong>les maladies neurodégénératives</strong> (sclérose en plaques,
            maladie de Parkinson, maladie d’Alzheimer et apparentées, …).
            J’accueille également les personnes souffrant de
            <strong>pathologies psychiatriques</strong> (schizophrénie, trouble
            bipolaire, …) et <strong>gérontopsychiatriques</strong>. Enfin, je suis
            investie dans l’évaluation des personnes présentant des personnes
            présentant un <strong>Haut Potentiel Intellectuel</strong>.
        </p>
        <p>
            Ma pratique professionnelle s’appuie sur mes expériences au sein des
            services de consultation mémoire des Centres Hospitaliers de Saint-Malo
            et de Redon-Carentoir ainsi que du Centre Mémoire de Recherche et
            Ressource du CHU de Nantes. J’ai également exercé au sein du Centre de
            Long Séjour du Groupement Hospitalier de Territoire de Nevers, de
            l’Unité Sanitaire en Milieu Pénitentiaire du Centre Hospitalier de
            Fontenay-le-Comte et de l’IME Val de Loire de La Flèche.
            <strong>Je travaille actuellement à temps partiel au sein de l’Institut
                Médico-Educatif Chantemerle à Bagneux</strong>.
        </p>
