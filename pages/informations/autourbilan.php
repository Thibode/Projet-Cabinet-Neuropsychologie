    <h2>Autour du bilan</h2>
    <div class="underline"></div>
    <p>
        Une ordonnance médicale n’est pas nécessaire au bilan. Cependant, il est fortement recommandé d’<strong>apporter
            tous
            les éléments et documents permettant de mieux comprendre votre demande</strong>(comptes-rendus médicaux et
        d’imagerie
        cérébrale, carnet de santé, ordonnances, …).
    </p>

    <p>
        Si les patients sont porteurs de <strong>lunettes</strong> ou d’<strong>appareils auditifs</strong>, il est
        essentiel de les avoir lors des
        séances.
    </p>
    <p>
        Au domicile, un <strong>climat de calme</strong> est nécessaire afin que le patient puisse se concentrer et
        s’investir pleinement
        dans le bilan sans être perturbé par des stimuli et sollicitations diverses.
    </p>
