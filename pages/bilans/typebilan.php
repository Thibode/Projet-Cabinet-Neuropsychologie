<div class="global">
    <h2>Les types de bilans</h2>
    <div class="underline"></div>
    <p>
        On distingue 2 types de bilans neuropsychologiques :
    </p>
    <div class="list">
        <p>
            <span class="change">♦</span><strong>le bilan psychométrique :</strong> il consiste en l’administration de
            l’échelle de Wechsler<span class="notecolor">*</span> qui permet d’évaluer le niveau et le potentiel
            intellectuel global du patient.
        </p>
        <p>
            <span class="change">♦</span><strong>le bilan neuropsychologique spécifique :</strong> il consiste en
            l’administration de tests neuropsychologiques afin d’évaluer spécifiquement différentes fonctions
            cognitives.
        </p>
        <div class="note">
            <p>
                <span class="notecolor">*</span> l’échelle de Wechsler utilisée est choisie selon l’âge du patient : la
                WPPSI-IV pour les enfants de 2 ans – 6 mois à 7 ans – 7 mois – le WISC-V pour les enfants et adolescents
                de 6 ans – 11 mois à 16 ans – 11 mois et la WAIS-IV pour les adolescents et adultes de 16 ans – 11 mois
                à 79 ans – 11 mois
            </p>
        </div>
        <div class="positionimg">
            <img src="./public/medias/Illustration-les-objectifs-du-bilan-300x284.png" />
        </div>
        <p>
            Le neuropsychologue choisit le type de bilan adapté à la demande du patient. Il est régulièrement nécessaire
            d’associer un bilan psychométrique à un bilan neuropsychologique spécifique afin de bien observer le
            fonctionnement neuropsychologique et intellectuel du patient dans son ensemble.
        </p>
    </div>
</div>