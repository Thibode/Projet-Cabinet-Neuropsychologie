    <h2>Déroulement du bilan</h2>
    <div class="underline"></div>
    <p>
        Un bilan neuropsychologique comprend toujours :
    </p>
    <div class="list">
        <p>
            <span class="change">♦</span><strong>un entretien clinique d’anamnèse.</strong> C’est le premier rendez-vous
            de rencontre
            avec le patient et si possible un proche<span class="notecolor">*</span>. Cet entretien permet de cibler le
            motif de consultation et de
            recueillir l’histoire personnelle et médicale. A la suite de cet entretien, le neuropsychologue propose des
            tests neuropsychologiques choisis selon la demande formulée et en lien avec ses observations cliniques.
        </p>
        <p>
            <span class="change">♦</span><strong>l’évaluation neuropsychologique.</strong> Elle se déroule en une ou
            plusieurs séances selon le motif de consultation et les dispositions du patient. Les séances d’évaluation se
            déroulent individuellement avec le patient uniquement. Les tests choisis par le neuropsychologue sont
            élaborés, normés et validés. Ils sont adaptés à l’âge du patient et à sa problématique.
        </p>
        <img src="./public/medias/Illustration-déroulement-du-bilan.png" alt="Illustration">
        <p>
            <span class="change">♦</span><strong>la cotation, l’interprétation et la rédaction d’un compte-rendu
                complet.</strong> :
            Suite à l’évaluation, le neuropsychologue cote et interprète les résultats obtenus par le patient et les
            mets en relation avec les éléments évoqués lors de l’entretien d’anamnèse ainsi qu’avec ses observations. Il
            rédige ensuite un compte-rendu qui propose des éléments d’explication, des conseils et indications répondant
            à la demande initiale du patient. Le compte-rendu oriente également vers des prises en charge adaptées selon
            les difficultés rencontrées. Il est adressé au patient et/ou à ses proches après le rendez-vous de
            restitution et peut également être envoyé au médecin traitant ainsi qu’aux spécialistes.
        </p>
        <p>
            <span class="change">♦</span><strong>un entretien de restitution orale.</strong> La restitution orale
            constitue le dernier rendez-vous du bilan neuropsychologique. Il permet au neuropsychologue de revenir sur
            le bilan réalisé et de présenter les résultats, les interprétations et les éléments d’explications avancés.
            La restitution permet également d’orienter vers une prise en charge adaptée (neuropsychologique,
            orthophonique, psychologique, …), vers d’éventuelles adaptations du quotidien ainsi que vers des
            préconisations thérapeutiques.
        </p>

    </div>
    <div class="note">
        <p>
            <span class="notecolor">*</span> obligatoire pour les mineurs
        </p>
