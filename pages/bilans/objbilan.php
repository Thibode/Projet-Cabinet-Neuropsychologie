<div class="global">
    <h2>Les objectifs du bilan</h2>
    <div class="underline"></div>
    <p>
        L’évaluation neuropsychologique poursuit nécessairement un objectif et
        s’inscrit dans une démarche qui peut être :
    </p>
    <div class="imgplace">
        <img src="./public/medias/Illustration-les-objectifs-du-bilan-300x284.png" alt="" />
    </div>
    <div class="list">
        <p>
            <span class="change">♦</span><strong>à visée diagnostique</strong> :
            contribuer au diagnostic d’une maladie
        </p>
        <p>
            <span class="change">♦</span><strong>à visée exploratoire</strong> :
            évaluer les conséquences et le retentissement cognitif, comportemental
            et émotionnel d’une pathologie connue
        </p>
        <p>
            <span class="change">♦</span><strong>à visée pronostique</strong> :
            évaluer l’évolution des troubles suite (ou non) à une prise en charge
            médicamenteuse, chirurgicale ou rééducatrice
        </p>
        <p>
            <span class="change">♦</span><strong>à visée de soin</strong> :
            établir un projet de soin individuel et personnel selon les besoins et
            les attentes du patient
        </p>
    </div>
    <p>
        Selon la demande, la durée d’un bilan peut varier de 2 à 6 heures,
        réparties sur plusieurs séances. L’organisation du bilan et la durée des
        séances se fait selon les disponibilités et la fatigabilité du patient.
    </p>
</div>